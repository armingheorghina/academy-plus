#include "bsq.h"

void	ft_first_col_one(char **map, int **m, t_param *param)
{
	int i;

	i = 0;
	while (i < param->lin)
	{
		if (map[i][0] == param->vid)
			m[i][0] = 1;
		else
			m[i][0] = 0;
		i++;
	}
}
