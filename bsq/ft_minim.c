int		minim(int a, int b, int c)
{
	int minim;

	minim = 0;
	if (a <= b && a <= c)
		minim = a;
	if (b <= a && b <= c)
		minim = b;
	if (c <= a && c <= b)
		minim = c;
	return (minim);
}
