#include "bsq.h"

void	ft_first_line_one(char **map, int **m, t_param *param)
{
	int j;

	j = 0;
	while (j < param->col)
	{
		if (map[0][j] == param->vid)
			m[0][j] = 1;
		else
			m[0][j] = 0;
		j++;
	}
}
