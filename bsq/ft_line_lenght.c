#include "bsq.h"

int		ft_line_lenght(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0' || str[i] == '\n')
		return (0);
	else
		while (str[i] != '\n')
		{
			i++;
		}
		return (i);
}
