#include "bsq.h"

int		ft_find_map_lines(char **str)
{
	int i;
	int j;
	int c;

	i = 0;
	j = 0;
	c = 0;
	while (str[i] != 0)
	{
		while (str[i][j] != '\n')
		{
			j++;
		}
		j = 0;
		i++;
		c++;
	}
	return (c);
}
